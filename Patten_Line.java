import java.util.*;
public class Patten_Line {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter Number To Print");
        int patten= sc.nextInt();
        
        for(int i=1;i<=patten;i++){
         for(int j=1;j<=patten;j++){
               System.out.print("*");
         }
         System.out.println();
         
        }

    }
}

